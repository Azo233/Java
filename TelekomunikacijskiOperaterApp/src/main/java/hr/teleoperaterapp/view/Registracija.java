    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        
          if(!Arrays.equals(pswLozinka.getPassword(), pswLozinkaPonovo.getPassword())){
            JOptionPane.showMessageDialog(null, "Lozinke ne odgovaraju");
            return;
        }
          
          Operater o= new Operater();
          o.setIme(txtIme.getText());
          o.setPrezime(txtPrezime.getText());
          o.setEmail(txtEmail.getText());
          o.setOib(txtOib.getText());
          o.setLozinka(BCrypt.hashpw(new String(pswLozinka.getPassword()), BCrypt.gensalt()));
          obrada.setEntitet(o);
          try {
              obrada.create();
              JOptionPane.showMessageDialog(null, "Uspje≈°no registrirani, prijavite se s novim podacima");
              dispose();
        } catch (OperaterException oe) {
              System.out.println(oe.getPoruka());
        }
    }//GEN-LAST:event_jButton1ActionPerformed

